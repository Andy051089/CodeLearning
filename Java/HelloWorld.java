package hello;

public class HelloWorld {
	/** 
	public 公開公共的 
	class 類別 
	HelloWorld 叫啥一個類的名子 通常跟檔名一樣方便
	{}表示範圍
*/
	public static void main(String[] args) {
		/**	
		static 靜態靜止的
		void 返回值類型(空,沒有返回值)
		main 表示主函數名稱
		main後面() 表示函數的參數列表
		String[] 表示字符串數組
		args 表示字符串數組名稱,可自行更改,通常使用args
	*/
		System.out.print("helloworld");
	}
}
